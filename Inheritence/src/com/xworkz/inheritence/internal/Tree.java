package com.xworkz.inheritence.internal;

public class Tree {

    public Tree() {
        System.out.println("No-arg const is running in Tree");
    }

    public void grow() {
        System.out.println("Tree is growing");
    }
    public void shedLeaves() {
        System.out.println("Tree is shedding leaves");
    }
    public void photosynthesize() {
        System.out.println("Tree is photosynthesizing");
    }
    public void provideOxygen() {
        System.out.println("Tree is providing oxygen");
    }
    public void bearFruit() {
        System.out.println("Tree is bearing fruit");
    }
}
