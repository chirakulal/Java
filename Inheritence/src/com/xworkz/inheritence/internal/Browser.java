package com.xworkz.inheritence.internal;


public class Browser {
    public Browser() {
        System.out.println("Running no-arg constructor of Browser");
    }

    public void open() {
        System.out.println("Browser can open websites");
    }

    public void close() {
        System.out.println("Browser can be closed");
    }

    public void navigate() {
        System.out.println("Browser allows navigation between web pages");
    }

    public void incognitoMode() {
        System.out.println("Browser supports private browsing mode");
    }

    public void extensions() {
        System.out.println("Browser supports extensions and add-ons");
    }
}

