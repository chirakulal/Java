package com.xworkz.inheritence.internal;

public class GlassObject {
    public GlassObject() {
        System.out.println("No-arg constructor is running in GlassObject");
    }

    public void reflect() {
        System.out.println("Reflecting light");
    }
    public void clean() {
        System.out.println("Cleaning the glass object");
    }
    public void breakGlass() {
        System.out.println("Breaking the glass");
    }
    public void mount() {
        System.out.println("Mounting the glass object");
    }
    public void frame() {
        System.out.println("Framing the glass");
    }
}
