package com.xworkz.inheritence.internal;

public class Fish {
    public Fish() {
        System.out.println("No-arg constructor is running in Fish");
    }

    public void swim() {
        System.out.println("Swimming in water");
    }
    public void breatheUnderwater() {
        System.out.println("Breathing underwater");
    }
    public void eatPlankton() {
        System.out.println("Eating plankton");
    }
    public void layEggs() {
        System.out.println("Laying eggs");
    }
    public void camouflage() {
        System.out.println("Camouflaging in the ocean");
    }
}
